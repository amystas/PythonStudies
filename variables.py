#variables

#string - series of characters
firstName = 'Amy'
lastName = 'Smith'
fullName = firstName + ' ' + lastName
print('Hello ' + fullName)
print(type(fullName))

#int - whole integer
age = 21
age += 1
print(age)
print(type(age))
print('Your age is: ' + str(age)) #type casting
print('Your age is: ', age)

#float - floating point numbers - numeric values with decimal
height = 5.8
print('Your height is: ' + str(height) + ' feet')
print(type(height))

#boolean - stores only true or false
human = False
print(human)
print(type(human))
print('Are you a human: ' + str(human))

#multiple assignment
name , age , attractive = 'Amy', 16, False
print(name, age, attractive)

Spongebob = Patrick = Sandy = Squidward = 30
print(Spongebob, Squidward)





